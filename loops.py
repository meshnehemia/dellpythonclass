# while loop

number = 20
while number <= 25:
    print(number)
    number += 1

# Decrementing values
a = 10
while a >= 1:
    print(a)
    a -= 1

# For loop

languages = ["python", "PHP", "kotlin", "C++"]
print(languages)
for language in languages:
    print(language)

for i in range(10):
    print(i)

for i in range(15, 20):
    print(i)

for i in range(0, 30, 2):
    print(i)
